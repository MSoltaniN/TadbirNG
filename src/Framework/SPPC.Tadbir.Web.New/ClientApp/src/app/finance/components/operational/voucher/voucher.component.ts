  editDataItem?: Voucher = undefined;

  returnFromCommitGroup: boolean = false;
  finalizedGroup: boolean = false;
  commitGroup: boolean = false;
  returnFromFinalizedGroup: boolean = false;
    
  UnConfirmGroupConfirm: boolean;
