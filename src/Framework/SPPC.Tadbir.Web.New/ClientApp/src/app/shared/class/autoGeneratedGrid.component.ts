  getRowValue(columnName: string, dataItem: any, dataType: string): any {
    
    var colName = columnName.charAt(0).toLowerCase() + columnName.slice(1);
    
    var res = dataItem[colName];
    var result = res;

    switch (dataType.toLowerCase()) {
      case "date": {
        result = res;
        break;
      }
      case "boolean": {
        if (res == true) {
          result = "فعال"
        }
        else {
          result = "غیرفعال"
        }
        break;
      }
      case "money": {
        result = res;
        break;
      }
      default:
    }

    return result;
  }

